#include <iostream>
#include <string>

using namespace std;
// int, long long, char, float and double
int main()
{
    long long n1, n2;
    cin >> n1 >> n2;
    cout << n1 << " + " << n2 << " = " << n1 + n2 << endl;
    cout << n1 << " * " << n2 << " = " << n1 * n2 << endl;
    cout << n1 << " - " << n2 << " = " << n1 - n2 << endl;
    return 0;
}